"""создал функцию которая выводит все слова заглавными буквами"""


def str_up():
    string_up = input("Введите текст: ").upper()
    return string_up


"""создал функцию которая выводит первые буквы любого слова с заглавной"""


def str_title():
    string_title = input("Введите текст: ").title()
    return string_title
